void main()
{
  String grade = 'A';
  switch (grade)
  {
    case 'A':
      {
        print("Excellent");
        break;
      }
    case 'B':
      {
        print("Very Good");
        break;
      }
    case 'C':
      {
        print("Average");
        break;
      }
    default:
      {
        print("Invalid grade");
      }
  }
}
